    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        double valorHora = Double.parseDouble(txtValorHora.getText());
        char genero = ' ';
        if (optFeminino.isSelected()) genero = 'f';
        if (optMasculino.isSelected()) genero = 'm';
        
        dev = new Desenvolvedor(txtNome.getText(), txtCpf.getText());
        dev.setGenero(genero);
        dev.setValorHora(valorHora);
        devs.add(dev);
        
        txtNome.setText("");
        txtCpf.setText("");
        txtValorHora.setText("");
        optMasculino.setSelected(false);
        optFeminino.setSelected(false);
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void optMasculinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_optMasculinoActionPerformed
        if (optMasculino.isSelected()) {
            optFeminino.setSelected(false);
        }
    }//GEN-LAST:event_optMasculinoActionPerformed

    private void optFemininoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_optFemininoActionPerformed
        if (optFeminino.isSelected()) {
            optMasculino.setSelected(false);
        }
    }//GEN-LAST:event_optFemininoActionPerformed

    private void btnDesenvolvedoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesenvolvedoresActionPerformed
        String desenvolvedores = "";
        for (Desenvolvedor dev : devs) {
            desenvolvedores += dev.imprimir();
        }
        
        JOptionPane.showMessageDialog(rootPane, desenvolvedores);
    }//GEN-LAST:event_btnDesenvolvedoresActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        for (Desenvolvedor dev : devs) {
            if (dev.getCpf().equals(txtPesquisarCpf.getText())) {
                JOptionPane.showMessageDialog(rootPane, dev.imprimir());
            }
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        for (int i=0; i < devs.size(); i++) {
            if (devs.get(i).getCpf().equals(txtPesquisarCpf.getText())) {
                String nomeDev = devs.get(i).getNome();
                devs.remove(i);
                JOptionPane.showMessageDialog(rootPane, nomeDev + " foi apagado com sucesso.");
            }
        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnPesquisarPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarPagamentoActionPerformed
        for (Desenvolvedor dev : devs) {
            if (dev.getCpf().equals(txtPesquisarCpfPagamento.getText())) {
                lblDesenvolvedorResultado.setText(dev.getNome());
            }
        }
    }//GEN-LAST:event_btnPesquisarPagamentoActionPerformed

    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        for (Desenvolvedor dev : devs) {
            if (dev.getCpf().equals(txtPesquisarCpfPagamento.getText())) {
                int numHoras = Integer.parseInt(txtNumHoras.getText());
                JOptionPane.showMessageDialog(rootPane, dev.imprimir(numHoras));
            }
        }
    }//GEN-LAST:event_btnPagarActionPerformed

